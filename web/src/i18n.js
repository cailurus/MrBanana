function interpolate(template, vars) {
    if (!template) return '';
    const v = vars && typeof vars === 'object' ? vars : {};
    return String(template).replace(/\{(\w+)\}/g, (_, k) => (typeof v[k] === 'undefined' ? `{${k}}` : String(v[k])));
}

export function t(lang, key, vars) {
    const l = UI_TEXT[lang] ? lang : 'zh-CN';
    const table = UI_TEXT[l] || {};
    const fallback = UI_TEXT['zh-CN'] || {};
    const template = table[key] ?? fallback[key] ?? String(key);
    return interpolate(template, vars);
}

export function detectDefaultUiLang() {
    try {
        const nav = (navigator && navigator.language) ? navigator.language : '';
        if (/^zh-?tw/i.test(nav) || /hant/i.test(nav)) return 'zh-TW';
        if (/^zh/i.test(nav)) return 'zh-CN';
    } catch {
        // ignore
    }
    return 'zh-CN';
}
